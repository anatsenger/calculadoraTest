package service;


public class CalculadoraService {

    public int somar(int a, int b) {
        return a+b;
    }

    public int subtrair(int a, int b) {
        return a-b;
    }

    public double dividir(double a, double b) {
        return a/b;
    }

    public double multiplicar(double a, double b) {
        return a*b;
    }
}
